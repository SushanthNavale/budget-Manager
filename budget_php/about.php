<h5 style="color:white;font-family:verdana;">&#9734 About MSRIT</h5>
<p align="justify" style="color:white;">
M.S. Ramaiah Institute of Technology (MSRIT) is a self financing institution established in Bangalore in the year 1962 by the industrialist and philanthropist, Late Dr. M S Ramaiah. MSRIT, as well as 20 other reputed institutions offering Medical, Dental, Pharmacy, Hotel, and Business Management, Legal Education and M.S. Ramaiah University of Applied Sciences are managed by the Gokula Education Foundation (GEF).  The mission statement of GEF is "To deliver education and health for the betterment of mankind".  MSRIT has an ISO 9001:2008 certification from BUREAU VERITAS(BV). All Engineering Departments offering Bachelor degree programmes have been accredited two to three times by NBA.  MSRIT  is  one  of  the few institutes  with  faculty- student  ratio of 1 : 15 and achieves excellent academic results as  indicated by the  significant number of  VTU ranks bagged  every year. The institute is a participant of the Technical Education Quality Improvement Programme (TEQIP) Phase - I and Phase II, an initiative of the Government of India. All the departments are having competent faculty, over 100% of them being postgraduates,  PhD holders. Some of the distinguished features of MSRIT are: State of the Art Laboratories, Individual computing facility to all faculty members, Campus-wide networking connecting all 2300 systems to the net and wifi activated.  All research departments are active with sponsored projects and more than 130 scholars are pursuing PhD. The Centre for  Advanced  Training and Continuing Education and Entrepreneurship Development Cell  have been set up on campus. MSRIT  has a strong Placement and Training Department with a committed team, a fully equipped Sports Department, Large Air-conditioned Library with over 1,20,300 books with subscription to more than 300 International and National Journals.   The Digital Library subscribes to several online e-journals like IEEE, ASCE Digital Library, Springerlink, Taylor and Francis, J-GATE, EBSCO Management Business Collection, DOAJ etc. MSRIT is a member of DELNET Consortium. MSRIT has a modern auditorium, several hi-tech conference halls, all air-conditioned with video-conferencing facilities. It has excellent hostel facilities for boys and girls.  MSRIT Alumni have distinguished themselves by occupying high positions in India and abroad and are in touch with the Institute through an active Alumni Association.  MSRIT obtained Academic Autonomy for all its UG and PG programmes in the year 2007 onwards..</p>
